    private void btnXacNhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXacNhanActionPerformed
        if (txtChip.getText().trim().isEmpty()
                || txtGiaNhap.getText().trim().isEmpty()
                || txtGiaBan.getText().trim().isEmpty()
                || txtGiaBan.getText().trim().isEmpty()
                || txtHDH.getText().trim().isEmpty()
                || txtMan.getText().trim().isEmpty()
                || txtPin.getText().trim().isEmpty()
                || txtTenSP.getText().trim().isEmpty()
                || txtThuongHieu.getText().trim().isEmpty()
                || txtXuatXu.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đủ thông tin!", "Thiếu thông tin", JOptionPane.ERROR_MESSAGE);
        } else {
            boolean isOK = true;
            SanPham sp = new SanPham();
            sp.setTenSP(txtTenSP.getText().trim());
            sp.setSoLuong(0);
            sp.setThuongHieu(txtThuongHieu.getText().trim());
            sp.setHeDieuHanh(txtHDH.getText().trim());
            sp.setChipXuLy(txtChip.getText().trim());
            sp.setXuatXu(txtXuatXu.getText().trim());
            try {
                sp.setKichThuocMan(Double.parseDouble(txtMan.getText().trim()));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Kích thước màn chỉ nhập số", "Lỗi", JOptionPane.ERROR_MESSAGE);
                isOK = false;
            }
            try {
                sp.setDungLuongPin(Integer.parseInt(txtPin.getText().trim()));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Dung lượng pin chỉ nhập số", "Lỗi", JOptionPane.ERROR_MESSAGE);
                isOK = false;
            }
            try {
                sp.setGiaNhap(Integer.parseInt(txtGiaNhap.getText().trim()));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Giá nhập chỉ nhập số", "Lỗi", JOptionPane.ERROR_MESSAGE);
                isOK = false;
            }
            try {
                sp.setGiaBan(Integer.parseInt(txtGiaBan.getText().trim()));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Giá bán chỉ nhập số", "Lỗi", JOptionPane.ERROR_MESSAGE);
                isOK = false;
            }
            if (isOK) {
                if (new SanPhamDAO().ThemSanPham(sp)) {
                    JOptionPane.showMessageDialog(this, "Thêm thành công!");
                    setVisible(false);
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm thất bại!");
                }
            }
        }
    }//GEN-LAST:event_btnXacNhanActionPerformed

