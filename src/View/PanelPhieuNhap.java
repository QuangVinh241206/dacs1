    private void showTableSP() {
        listSP = new SanPhamDAO().getListSP();
        DefaultTableModel model = (DefaultTableModel) tableSanPham.getModel();
        model.setRowCount(0);
        for (SanPham sp : listSP) {
            model.addRow(new Object[]{sp.getMaSP(), sp.getTenSP(),
                sp.getSoLuong()});
        }
    }

    private void showGioHang(ArrayList<CTPhieuNhap> list) {
        DefaultTableModel model = (DefaultTableModel) tableGiohang.getModel();
        model.setRowCount(0);
        STT = 0;
        TongTien = 0;
        for (CTPhieuNhap ctpn : list) {
            SanPham sp = new SanPhamDAO().getSPbyID(ctpn.getMaSP());
            Object[] giohang = {++STT, ctpn.getMaSP(), sp.getTenSP(), tien.format(ctpn.getGia()) + "đ", ctpn.getSoLuong()};
            model.addRow(giohang);
        }
    }

    private void showTable() {
        listPN = new PhieuNhapDAO().getListPhieuNhap();
        DefaultTableModel model = (DefaultTableModel) tablePN.getModel();
        model.setRowCount(0);
        for (PhieuNhapKho pn : listPN) {
            NhanVien nv = new NhanVienDAO().getNhanVienByID(pn.getMaNVNhap());
            NhaCungCap ncc = new NhaCungCapDAO().getNCCByID(pn.getMaNCC());
            model.addRow(new Object[]{pn.getMaPN(), ncc.getTenNCC(), nv.getTenNV(), pn.getThoiGian(), tien.format(pn.getTongTien()) + "đ"});
        }
    }

    private void tinhTien() {
        if (listGioHang.size() == 0) {
            txtTongTien.setText("0đ");
            TongTien = 0;
        } else {
            for (CTPhieuNhap i : listGioHang) {
                TongTien += i.getGia() * i.getSoLuong();
            }
            txtTongTien.setText(tien.format(TongTien) + "đ");
        }
    }

