    private int TangMaPN() {
        int mapn = 0;
        listPN = new PhieuNhapDAO().getAllPhieuNhap();
        if (listPN.size() == 0) {
            mapn = 1;
        } else {
            PhieuNhapKho pn = new PhieuNhapKho();
            pn = listPN.get(listPN.size() - 1);
            mapn = pn.getMaPN() + 1;
        }
        return mapn;
    }

