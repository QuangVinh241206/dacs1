    private void filterPhieuNhap() {
        String khachHang = boxLocKH.getSelectedItem() != null
                ? boxLocKH.getSelectedItem().toString()
                : "Tất cả";
        String nhanVien = boxLocNV.getSelectedItem() != null
                ? boxLocNV.getSelectedItem().toString()
                : "Tất cả";

        DefaultTableModel model = (DefaultTableModel) tablePX.getModel();
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
        tablePX.setRowSorter(sorter);

        List<RowFilter<Object, Object>> filters = new ArrayList<>();

        if (!khachHang.equals("Tất cả")) {
            filters.add(RowFilter.regexFilter(Pattern.quote(khachHang), 1)); // cột 1: Nhà cung cấp
        }

        if (!nhanVien.equals("Tất cả")) {
            filters.add(RowFilter.regexFilter(Pattern.quote(nhanVien), 2)); // cột 2: Nhân viên nhập
        }

        if (filters.isEmpty()) {
            sorter.setRowFilter(null); // Không lọc gì cả
        } else {
            sorter.setRowFilter(RowFilter.andFilter(filters));
        }
    }

    private void fillBoxLoc() {
        listNV = new NhanVienDAO().getListNV();
        listKH = new KhachHangDAO().getListKH();
        boxLocKH.removeAllItems();
        boxLocNV.removeAllItems();
        boxLocKH.addItem("Tất cả");
        boxLocNV.addItem("Tất cả");
        for (NhanVien nv : listNV) {
            boxLocNV.addItem(nv.getTenNV());
        }
        for (KhachHang kh : listKH) {
            boxLocKH.addItem(kh.getTenKH());
        }
    }

