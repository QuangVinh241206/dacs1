    private void btnNhapHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNhapHangActionPerformed
        if (tableGiohang.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Chưa thêm sản phẩm cần nhập!");
        } else {
            PhieuXuatKho px = new PhieuXuatKho();
            px.setMaPX(TangMaPX());
            px.setMaKH(getChonMaKH(boxKH.getSelectedIndex()));
            px.setMaNV(nhanvien.getMaNV());
            px.setTongTien(TongTien);
            int dongy = JOptionPane.showConfirmDialog(this, "bạn có chắc chắn muốn tạo phiếu?", "Thông báo", JOptionPane.YES_NO_OPTION);
            if (dongy == JOptionPane.YES_OPTION) {
                if (new PhieuXuatDAO().ThemPhieuXuat(px)) {
                    for (CTPhieuXuat ctpx : listGioHang) {
                        new CTPhieuXuatDAO().ThemCTPX(ctpx);
                        new SanPhamDAO().TruSoLuong(ctpx.getMaSP(), ctpx.getSoLuong());
                    }
                    JOptionPane.showMessageDialog(this, "Tạo thành công");
                    setDefault();
                } else {
                    JOptionPane.showMessageDialog(this, "Tạo thất bại!");
                }
            }
        }
    }//GEN-LAST:event_btnNhapHangActionPerformed

