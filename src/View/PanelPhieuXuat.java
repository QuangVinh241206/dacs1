    private void showTableSP() {
        listSP = new SanPhamDAO().getListSP();
        DefaultTableModel model = (DefaultTableModel) tableSanPham.getModel();
        model.setRowCount(0);
        for (SanPham sp : listSP) {
            model.addRow(new Object[]{sp.getMaSP(), sp.getTenSP(),
                sp.getSoLuong()});
        }
    }

    private void showGioHang(ArrayList<CTPhieuXuat> list) {
        DefaultTableModel model = (DefaultTableModel) tableGiohang.getModel();
        model.setRowCount(0);
        STT = 0;
        TongTien = 0;
        for (CTPhieuXuat ctpx : list) {
            SanPham sp = new SanPhamDAO().getSPbyID(ctpx.getMaSP());
            Object[] giohang = {++STT, ctpx.getMaSP(), sp.getTenSP(), tien.format(ctpx.getGia()) + "đ", ctpx.getSoLuong()};
            model.addRow(giohang);
        }
    }

    private void showTable() {
        listPX = new PhieuXuatDAO().getListPhieuXuat();
        DefaultTableModel model = (DefaultTableModel) tablePX.getModel();
        model.setRowCount(0);
        for (PhieuXuatKho px : listPX) {
            NhanVien nv = new NhanVienDAO().getNhanVienByID(px.getMaNV());
            KhachHang kh = new KhachHangDAO().getKHbyID(px.getMaKH());
            model.addRow(new Object[]{px.getMaPX(), kh.getTenKH(), nv.getTenNV(), px.getThoiGian(), tien.format(px.getTongTien()) + "đ"});
        }
    }

    private void tinhTien() {
        if (listGioHang.size() == 0) {
            txtTongTien.setText("0đ");
            TongTien = 0;
        } else {
            for (CTPhieuXuat i : listGioHang) {
                TongTien += i.getGia() * i.getSoLuong();
            }
            txtTongTien.setText(tien.format(TongTien) + "đ");
        }
    }

