    private int TangMaPX() {
        int mapx = 0;
        listPX = new PhieuXuatDAO().getAllPhieuXuat();
        if (listPX.size() == 0) {
            mapx = 1;
        } else {
            PhieuXuatKho pn = new PhieuXuatKho();
            pn = listPX.get(listPX.size() - 1);
            mapx = pn.getMaPX() + 1;
        }
        return mapx;
    }

