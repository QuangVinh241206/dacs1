    private void getSelectRowSP() {
        tableSanPham.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                listSP = new SanPhamDAO().getListSP();
                tableGiohang.clearSelection();
                btnSua.setEnabled(false);
                btnXoa.setEnabled(false);
                int SelectedRow = tableSanPham.getSelectedRow();
                if (SelectedRow == -1) {
                } else {
                    SanPham sp = listSP.get(SelectedRow);
                    txtMaSP.setText(sp.getMaSP() + "");
                    txtTenSP.setText(sp.getTenSP());
                    txtGiaXuat.setText(tien.format(sp.getGiaBan()) + "đ");
                    btnThemGio.setEnabled(true);
                    txtSoLuong.setText("");
                }
            }
        });
    }

